package simplehtest;

public class basePlayer extends baseChar {
	
	private int exp;
	private int tnl;
	private int wid;
	
	
	
	
	
	
	
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public int getTnl() {
		return tnl;
	}
	public void setTnl(int tnl) {
		this.tnl = tnl;
	}
	public int getWid() {
		return wid;
	}
	public void setWid(int wid) {
		this.wid = wid;
	}
	
	

	
}

